# smiley face 
import turtle
turtle.penup()
turtle.pensize(3)
turtle.goto(0,-75)
turtle.pendown()
turtle.circle(75)
turtle.penup()
turtle.begin_fill()
turtle.goto(25,25)
turtle.circle(10)
turtle.end_fill()
turtle.penup()
turtle.goto(-25,25)
turtle.pendown()
turtle.begin_fill()
turtle.circle(10)
turtle.end_fill()
turtle.penup()
turtle.goto(-15,10)
turtle.pendown()
turtle.goto(0,20)
turtle.goto(15,10)
turtle.penup()
turtle.color("red")
turtle.goto(-26,-25)
turtle.pendown()
turtle.goto(0,-35)
turtle.goto(26,-25)
turtle.hideturtle()
turtle.done()